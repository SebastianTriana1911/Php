<?php
/* Cuando hablamos de operadores de comparacion son los
simbolos que nos permiten comparar una cosa con la otra
y estos son algunos de esos */
$a = 1;
$b = 2;

// Es igual a "=="
echo $a == $b;

// Es diferente de "!=" o "<>"
echo $a != $b;
echo $a <> $b;

// Un valor es y el valor es
echo $a && $b; 

// Un valor es o el valor es
echo $a || $b;
?>