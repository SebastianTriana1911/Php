<?php
$a = 5;

// El operador de asignacion "++" aumenta en una unidad el valor que
// tenga la variable que se le pase
$a ++;
echo $a;

// Tambien se podria de interpretar de esta manera
++ $a;
echo $a;

// El operador de asignacion "--" disminuye en una unidad el valor que
// tenga la variable que se le pase
$a --;
echo $a;

// Tambien se podria de interpretar de esta manera
-- $a;
echo $a;

?>